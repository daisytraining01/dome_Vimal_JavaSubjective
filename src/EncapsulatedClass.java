public class EncapsulatedClass {

	// Private variables declared
	private String userName;
	private int empId;

	// Getter methods
	public String getUserName() {
		return userName;
	}

	// Setter methods
	public void setUserName(String userName) {
		this.userName = userName;
	}

	// Getter methods
	public int getEmpId() {
		return empId;
	}

	// Setter methods
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	

}
